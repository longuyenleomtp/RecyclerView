package com.example.btrecyclerview;

public class Computer {
    public String mName;
    public int giatien;

    public void setName(String mName) {
        this.mName = mName;
    }

    public void setGia(int gia) {
        this.giatien = giatien;
    }

    public Computer(String mName, int gia) {
        this.mName = mName;
        this.giatien = giatien;
    }

    public String getmName() {
        return mName;
    }

    public int getGia() {
        return giatien;
    }
}
